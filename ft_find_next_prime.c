#include "libft.h"

int		ft_find_next_prime(int nb)
{
	int a;

	while (1)
	{
		a = 2;
		while (nb % a != 0)
			a++;
		if (nb == a)
			return (nb);
		else
			nb++;
	}
	return (0);
}
